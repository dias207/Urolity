/* ══════════════════════════════════════════════════════════
   Применить язык
   ══════════════════════════════════════════════════════════ */
function applyLang(lang) {
  const t = translations[lang] || translations.ru;

  /* 1. Все элементы с data-lang → innerHTML */
  document.querySelectorAll('[data-lang]').forEach(el => {
    const key = el.getAttribute('data-lang');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  /* 2. Placeholders */
  document.querySelectorAll('[data-placeholder]').forEach(el => {
    const key = el.getAttribute('data-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  /* 3. Value (кнопки input) */
  document.querySelectorAll('[data-value]').forEach(el => {
    const key = el.getAttribute('data-value');
    if (t[key] !== undefined) el.value = t[key];
  });

  /* 4. Обновить кнопку переключателя */
  const btns = document.querySelectorAll('.lang-btn');
  btns.forEach(btn => {
    btn.classList.toggle('lang-active', btn.getAttribute('data-lang-switch') === lang);
  });

  /* 5. lang-атрибут на <html> */
  document.documentElement.lang = lang === 'kz' ? 'kk' : 'ru';

  /* 6. Сохранить */
  localStorage.setItem('siteLang', lang);
}

/* ══════════════════════════════════════════════════════════
   Вставить переключатель в header
   ══════════════════════════════════════════════════════════ */
function injectLangSwitcher() {
  const hdrBtns = document.querySelector('.hdr-btns');
  if (!hdrBtns) return;

  const switcher = document.createElement('div');
  switcher.className = 'lang-switcher';
  switcher.innerHTML = `
    <button class="lang-btn" data-lang-switch="ru" aria-label="Русский">RU</button>
    <span class="lang-sep">|</span>
    <button class="lang-btn" data-lang-switch="kz" aria-label="Қазақша">KZ</button>
  `;

  /* Вставить перед .hdr-btns */
  hdrBtns.parentNode.insertBefore(switcher, hdrBtns);

  switcher.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      applyLang(btn.getAttribute('data-lang-switch'));
    });
  });
}

